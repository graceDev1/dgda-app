const mongodb = require('../server/connect');


const AdminSchema = mongodb.Schema({
    name: {
        type: String,
        required: true
    },
    email: {
        type: String,
        required: true,
        unique: true
    },
    password: {
        type: String,
        required: true
    },
    register_date: {
        type: Date,
        default: Date.now
    }
});


module.exports = mongodb.model('Admin', AdminSchema);
